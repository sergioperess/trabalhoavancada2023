public class Combustivel {

        private static double gasolina;
        private static double etanol;

        public Combustivel() {
            gasolina = 3.40;
            etanol = 2.25;
        }

        public double getEtanol() {
            return etanol;
        }

        public double getGasolina() {
            return gasolina;
        }
    }
